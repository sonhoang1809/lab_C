#include <stdio.h>
 
int main()
{
    int a, b; // khai bao 2 bien kieu so nguyen
    float c; // khai bao 1 bien kieu so thuc
 
    // Gan gia tri cho cac bien
 
    a = 1;      
    b = 2;
    c = 3.4;
 
    // vua khai bao bien vua gan gia tri
    int e = 4, f = 6;
 
    printf("a = %d; b = %d\n", a, b);
    printf("c = %f\n", c);
    printf("e = %d; f = %d\n", e, f);
 
    return 0;
}
